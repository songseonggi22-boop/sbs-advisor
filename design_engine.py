# 파싱·매칭·설계 함수
# ══════════════════════════════════════════════════════════════════════════════

def _norm(text: str) -> str:
    return text.lower().replace(" ", "").replace("-", "").replace("_", "")


def find_field(query: str) -> str | None:
    """쿼리에서 가장 잘 매칭되는 분야 키를 반환한다."""
    q = _norm(query)
    best_key, best_score = None, 0
    for key, cfg in FIELDS.items():
        score = sum(len(kw) for kw in cfg["keywords"] if _norm(kw) in q)
        if score > best_score:
            best_score, best_key = score, key
    return best_key if best_score > 0 else None


def detect_style(query: str) -> str:
    """'min' / 'max' / 'both' 반환."""
    q = query.lower()
    is_min = any(k in q for k in ["최소","기초","입문","빠른","단기","가성비","핵심만","짧게","3개월"])
    is_max = any(k in q for k in ["최대","심화","풀","포트폴리오","하이엔드","전문","완성","취업","심화","6개월","1년"])
    if is_min and not is_max:
        return "min"
    if is_max and not is_min:
        return "max"
    return "both"


def design(query: str, price_map: dict[str, int]) -> dict | None:
    """
    쿼리를 분석해 커리큘럼 설계 결과를 반환한다.

    반환 형태:
    {
        "field_key":   str,
        "field_label": str,
        "style":       "both" | "min" | "max",
        "min": {
            "courses":   [str, ...],
            "total":     int,
            "intent":    str,
            "relations": [(course, desc), ...],
        },
        "max": { ... },
    }
    """
    field_key = find_field(query)
    if not field_key:
        return None

    cfg   = FIELDS[field_key]
    style = detect_style(query)

    def build_plan(names: list[str], intent: str) -> dict:
        valid = [c for c in names if c in price_map]
        extra = cfg.get("course_extra", {})
        relations = []
        for c in valid:
            base = COURSE_ROLES.get(c, "")
            ex   = extra.get(c, "")
            desc = f"{base}  \n  ↳ {ex}" if ex else base
            relations.append((c, desc))
        return {
            "courses":   valid,
            "total":     sum(price_map[c] for c in valid),
            "intent":    intent,
            "relations": relations,
        }

    return {
        "field_key":   field_key,
        "field_label": cfg["label"],
        "style":       style,
        "min":         build_plan(cfg["min_courses"], cfg["min_intent"]),
        "max":         build_plan(cfg["max_courses"], cfg["max_intent"]),
    }
